public abstract class Familia_Pessoa {
    protected String nome;
    protected String telefone;

    public Familia_Pessoa(String nome, String telefone) {
        this.nome = nome;
        this.telefone = telefone;
    }

    public String getNome() {
        return nome;
    }

    public String getTelefone() {
        return telefone;
    }
    
}
